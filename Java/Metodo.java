/**
 * método:
 * é um pedaço de código (sub-rotina)
 * disponibilizada por uma classe. que pode ser executada quando
 * é feita uma requisição dela. elas são  responsáveis por definir
 * e realizar um determinado comportamento
 *
 *
 * Padrões de definições:
 *
 *   Visibilidade:
 *   public;
 *   protected;
 *   private;
 *
 *   Tipo :
 *   concreto
 *   Abstrato
 *
 *   Retorno
 *   tipo de dado ou "void" <- vazio
 *
 *  Nome fornecido ao método
 *
 *   Parametro
 *
 *   exceções
 *   para prever erros possíveis
 *
 *
 *   Modificador:
 *   static
 *   final = variável constante que não irá mudar
 *
 *
 *
 *   Como utilizar um método
 *
 *   nome_da_classe.nome_do_metodo();
 *   nome_da_classe.nome_do_metodo(...);
 *
 *   nome_da_objeto.nome_do_metodo();
 *   nome_da_objeto.nome_do_metodo(...);
 *
 *   ex:
 *   usuario.getEmail();
 *   usuario.açterarEndereco(endereco);
 *
 *   Math.random();
 *   Math.sqrt(4);
 *
 *
 *
 **/




